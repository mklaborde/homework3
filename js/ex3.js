let num1=Number(prompt("Enter the 1st integer:"));
let num2=Number(prompt("Enter the 2nd integer:"));

console.log(`${num1} + ${num2} = ${num1+num2}`);
console.log(`${num1} - ${num2} = ${num1-num2}`);
console.log(`${num1} x ${num2} = ${num1*num2}`);
console.log(`${num1} / ${num2} = ${num1/num2}`);
console.log(`${num1} modulus ${num2} = ${num1%num2}`);